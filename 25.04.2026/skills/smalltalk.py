import random

smalltalk_responses = {
    "как дела": ["Хорошо, спасибо! А у вас?", "Отлично! Чем могу помочь?", "Всё отлично!"],
    "как ты": ["Я в порядке, спасибо!", "Нормально, работаю!", "Всё хорошо!"],
    "как настроение": ["Отличное настроение!", "Хорошее! А у вас?", "Прекрасное!"],
    "спасибо": ["Пожалуйста!", "Всегда рад помочь!", "Обращайтесь!"],
    "молодец": ["Спасибо!", "Рад стараться!", "Благодарю!"],
    "круто": ["Спасибо!", "Я стараюсь!", "Рад, что вам нравится!"],
}

default_responses = [
    "Интересно... Расскажите ещё!",
    "Понятно. Что ещё хотите спросить?",
    "Угу. А что по погоде?",
    "Да ладно! А я вот погоду смотрю."
]

def smalltalk_skill(text: str) -> str:
    text_lower = text.lower()
    
    for key, responses in smalltalk_responses.items():
        if key in text_lower:
            return random.choice(responses)
    
    return random.choice(default_responses)
